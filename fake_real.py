st.title("🔍 AI Image Detector")
st.write("Upload an image to determine if it was captured by a camera or generated by Artificial Intelligence.")

# Load Model
model = load_detector_model()

if model:
    # File Uploader
    uploaded_file = st.file_uploader("Choose an image...", type=["jpg", "jpeg", "png", "webp"])

    if uploaded_file is not None:
        # Display the uploaded image
        image = Image.open(uploaded_file).convert('RGB')
        st.image(image, caption='Uploaded Image', use_container_width=True)
        
        # Analyze Button
        if st.button("Analyze Image", type="primary"):
            with st.spinner('Analyzing pixel frequencies and artifacts...'):
                
                # Preprocess and Predict
                processed_tensor = preprocess_image(image)
                prediction = model.predict(processed_tensor)[0][0]
                
                # Calculate Confidence
                # Assuming standard alphabetical dataset loading: 0 = Fake, 1 = Real
                if prediction >= 0.5:
                    result = "REAL"
                    confidence = prediction * 100
                    color = "green"
                else:
                    result = "FAKE (AI Generated)"
                    confidence = (1 - prediction) * 100
                    color = "red"
                
                # Display Results
                st.markdown("---")
                st.subheader("Analysis Complete")
                
                # Use Markdown to display colored text
                st.markdown(f"<h2 style='text-align: center; color: {color};'>{result}</h2>", unsafe_allow_html=True)
                
                # Display Confidence Metrics
                st.write(f"**Confidence Score:** {confidence:.2f}%")
                st.progress(int(confidence))
                
                # Educational breakdown
                with st.expander("How does this work?"):
                    st.write("""
                    This model uses **EfficientNetB0** to analyze the image. 
                    Before analyzing, it applies a **Fast Fourier Transform (FFT) High-Pass Filter** to the image. 
                    AI generators (like Midjourney or DALL-E) often leave microscopic, high-frequency noise patterns in the pixels that human eyes cannot see. 
                    The filter exposes these hidden patterns so the AI can catch the fakes.
                    """)